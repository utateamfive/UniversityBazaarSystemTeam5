package com.teamfive.universitybazaarsystem;

public class Club {
    String ClubName;
    String ClubType;
    String ClubDescription;
    String ClubMembers;

    public String getClubName() {
        return ClubName;
    }

    public String getClubType() {
        return ClubType;
    }

    public String getClubDescription() {
        return ClubDescription;
    }

    public String getClubMembers() {
        return ClubMembers;
    }
}
